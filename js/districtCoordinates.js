/**
 * 根据区县/城市/省份名称获取坐标
 * @param {string} name - 区县/城市/省份名称
 * @returns {Array|null} [经度, 纬度] 或 null
 */
function getDistrictCoordinate(name) {
    if (!name) return null;
    
    // 清理名称（去掉首尾空格）
    const cleanName = name.trim();
    
    // 1. 直接匹配
    if (DISTRICT_COORDINATES[cleanName]) {
        return DISTRICT_COORDINATES[cleanName];
    }
    
    // 2. 尝试去掉"区"、"县"、"市"后缀匹配
    const nameWithoutSuffix = cleanName.replace(/[区县市]$/, '');
    
    if (DISTRICT_COORDINATES[nameWithoutSuffix + '区']) {
        return DISTRICT_COORDINATES[nameWithoutSuffix + '区'];
    }
    if (DISTRICT_COORDINATES[nameWithoutSuffix + '县']) {
        return DISTRICT_COORDINATES[nameWithoutSuffix + '县'];
    }
    if (DISTRICT_COORDINATES[nameWithoutSuffix + '市']) {
        return DISTRICT_COORDINATES[nameWithoutSuffix + '市'];
    }
    
    // 3. 尝试匹配城市名（加"市"后缀）
    if (DISTRICT_COORDINATES[cleanName + '市']) {
        return DISTRICT_COORDINATES[cleanName + '市'];
    }
    
    // 4. 尝试匹配省份名（加"省"后缀）
    if (DISTRICT_COORDINATES[cleanName + '省']) {
        return DISTRICT_COORDINATES[cleanName + '省'];
    }
    
    // 5. 特殊处理：如果名称包含"市"但坐标库需要精确匹配
    if (cleanName.endsWith('市') && DISTRICT_COORDINATES[cleanName]) {
        return DISTRICT_COORDINATES[cleanName];
    }
    
    return null;
}

